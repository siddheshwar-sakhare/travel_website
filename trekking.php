            <h3>Why to Trek <span>?</span></h3>
            <p>The Himalaya, the "abode of snows", extends from Assam in eastern India west to Afghanistan. It is a chain of the highest and youngest mountains on earth and it encompasses a region of deep religious and cultural traditions and an amazing diversity of people. A trek in Nepal is a special and rewarding mountain holiday. Just as New York is not representative of the USA, so Kathmandu is not representative of Nepal. If you have the time and energy to trek, don't miss the opportunity to leave Kathmandu and see the spectacular beauty and the unique culture of Nepal. Fortunately for the visitor, there are still only a few roads extending deeply into the hills, so the only way to truly visit the remote regions of the kingdom is in the slowest and most intimate manner - walking. It requires more time and effort, but the rewards are also greater.

Instead of zipping down a freeway, racing to the next "point of interest," each step provides new and intriguing viewpoints. You will perceive your day as an entity rather than a few highlights strung together by a ribbon of concrete. For the romanticist, each step follows the footsteps of Hillary, Tenzing, Herzog and other Himalayan explorers. If you have neither the patience nor the physical stamina to visit the hills of Nepal on foot, a helicopter flight provides an expensive and unsatisfactory substitute.

Trekking in Nepal will take you through a country that has captured the imagination of mountaineers and explorers for more than 100 years. You will meet people in remote mountain villages whose lifestyle has not changed in generations. Most people trust foreigners. Nepal is one of only a handful of countries that has never been ruled by a foreign power.</p>      
               <h3>When to Trek ?</h3>
            
<p>There are two major factors to weigh as you decide when to go to Nepal: crowds and weather. Generally, the better the weather, the more people come to Nepal to go trekking. During the high tourist season in October and November, flights and hotels are fully booked and hotels and trails in the hills can be horrendously busy.
During autumn the nights are cold in the mountains, but the bright sun makes for pleasant day temperatures - in the high 20s? C, falling to 5? C at night, between 1000 metres and 3500 metres. At higher altitudes temperatures range from about 20? C down to - 10? C. Mornings are usually clear with clouds building up during the afternoon, disappearing at night to reveal spectacular starry skies. During winter, it is about 10 degrees colder.

The monsoon is a good time to visit Kathmandu, but there are few trekkers among those who come. A monsoon trek is possible if you are willing to put up with the rain, leeches, slippery trails and lousy mountain views. Flights operate throughout the monsoon to Lukla, Jumla and Jomsom, so it is possible to fly in and trek above the leech line.

Many of the new treks to recently opened restricted areas are good summer treks. Mustang and Simikot are partially in the Himalayan rain shadow, so trekking conditions are good throughout the monsoon season. Most of the restricted area treks are impossible during the winter season.</p>
