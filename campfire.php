             <p>A new analysis of burned antelope bones from caves in Swartkrans, South Africa, confirms that Australopithecus robustus and/or Homo erectus built campfires roughly 1.6 million years ago.[1] Nearby evidence within Wonderwerk Cave, at the edge of the Kalahari Desert, has been called the oldest known controlled fire.[2] Microscopic analysis of plant ash and charred bone fragments suggests that materials in the cave were not heated above about 1,300 °F (704 °C). This is consistent with preliminary findings that the fires burned grasses, brush, and leaves. Such fuel would not produce hotter flames. The data suggests humans were cooking prey by campfire as far back as the first appearance of Homo erectus 1.9 million years ago.</p>
